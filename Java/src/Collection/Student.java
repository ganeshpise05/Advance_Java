package Collection;

// Create Blueprint of Student & add elements by creating an a object.

public class Student {

	String name;
	int id;
	String stream;
	int std;
	String email;
	
	public Student() {
		
	}
	
	public Student(String name,int id,String stream,int std,String email) {
		this.name = name;
		this.id = id;
		this.stream = stream;
		this.std = std;
		this.email = email;
	}
}
