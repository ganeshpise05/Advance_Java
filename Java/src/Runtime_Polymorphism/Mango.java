package Runtime_Polymorphism;

public class Mango extends Fruit {
	
	public void name(String name) {
		System.out.println("Name of fruit is: "+name);
	}
	
	public void origin(String origin) {
		System.out.println("Origin of fruit mango is:"+origin);
	}
	
	public void taste(String taste) {
		System.out.println(" Taste of mango is: "+taste);
	}
	
	public void price(double price) {
		System.out.println("Price of mango is: "+price );
		System.out.println("-----------------------");
	}


}
