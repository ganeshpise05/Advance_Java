package Runtime_Polymorphism;

//compiletime polymorphism which can occured at execution of program 

public class Animal {
	
	public void makeSound() {
		System.out.println("Animal makes a sound.");
	}
	
	public void legs() {
		System.out.println("Animal have legs");
	}
	
	public void behave() {
		System.out.println("Animals have good behaviour.");
	}

}
